#include "../includes/libft.h"

int		ft_atoi(const char *str)
{
	int					i;
	int					k;
	unsigned long long	n;

	i = 0;
	n = 0;
	k = 1;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
		i++;
	if (str[i] == 43 || str[i] == 45)
	{
		(str[i] == 45 ? k = -1 : k);
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
	{
		n = 10 * n + (str[i++] - 48);
		if (n >= 9223372036854775807 && k == 1 && str[9] != '7')
			return (-1);
		if (n >= 9223372036854775807 && k == -1 && str[10] != '8')
			return (0);
		if (n == 2147483648 && k == -1)
			return (-2147483648);
	}
	return ((int)n * k);
}
